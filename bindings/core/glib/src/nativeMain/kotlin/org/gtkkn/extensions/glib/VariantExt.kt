package org.gtkkn.extensions.glib

import kotlinx.cinterop.toKString
import org.gtkkn.bindings.glib.Variant
import org.gtkkn.native.glib.g_variant_get_string

public fun Variant.getString(): String =
    g_variant_get_string(this.glibVariantPointer, null)
        ?.toKString()
        ?: error("Expected not null string")
